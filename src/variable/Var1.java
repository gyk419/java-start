package variable;

public class Var1 {
    public static void main(String[] args) {
        // 리터럴 : 직접 입력한 고정된 값 --> (100, 10.5 ,true, A, AAAA)
        int a = 100;
        double b = 10.5;
        boolean c = true;
        char d = 'A';
        String e = "AAAA";

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
